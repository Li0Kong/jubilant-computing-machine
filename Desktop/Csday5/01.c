#include <stdio.h>

int main() {
    printf("\033[0;33m"); // 设置输出颜色为棕色
    printf("          ________\n");
    printf("       .-'        '-.\n");
    printf("      /              \\\n");
    printf("     |                |\n");
    printf("     |,  .-.  .-.  ,  |\n");
    printf("     | )(__/  \\__) ( |\n");
    printf("     |/     /\\      \\|\n");
    printf("     (_     ^^      _ )\n");
    printf("      \\__|IIIIII|__/\n");
    printf("       | \\IIIIII/ |\n");
    printf("       \\          /\n");
    printf("        `--------`\n");
    printf("\033[0m"); // 恢复默认颜色

    return 0;
}
